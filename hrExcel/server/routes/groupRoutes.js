import express from 'express';
import {
  createDefaultGroup,
  createCustomGroup,
  getEmployees,
  getGroups,
  updateEmployee,
  updateGroup,
  deleteEmployee,
  deleteGroup
} from '../controllers/groupController.js';

const router = express.Router();

// Create a default group
router.post('/default-group', createDefaultGroup);

// Create a custom group
router.post('/custom-group', createCustomGroup);

// Get all employees
router.get('/employees', getEmployees);

// Get all groups
router.get('/groups', getGroups);

// Update an employee
router.put('/employees/:employeeId', updateEmployee);

// Update a group
router.put('/groups/:groupId', updateGroup);

// Delete an employee
router.delete('/employees/:employeeId', deleteEmployee);

// Delete a group
router.delete('/deleteGroup/:groupId', deleteGroup);

export default router;
